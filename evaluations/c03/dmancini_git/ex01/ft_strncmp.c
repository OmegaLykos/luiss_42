int	ft_strncmp(char *s1, char *s2, unsigned int n)
{
	int				math;
	unsigned int	i;

	i = 0;
	math = 0;
	while ((i < n -1) && *s1 == *s2)
	{
		s1++;
		s2++;
		i++;
	}
	math = (*s1 - *s2);
	return (math);
}
