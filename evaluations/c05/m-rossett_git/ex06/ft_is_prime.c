int	ft_is_prime(int nb)
{
	int	primo;
	int	i;

	primo = 1;
	i = 2;
	while (i < nb)
	{
		if (nb % i == 0)
		{
			primo = 0;
			break ;
		}
		i++;
	}
	if (primo && nb != 1)
		return (1);
	else
		return (0);
}
