int	add(int val1, int val2)
{
	return (val1 + val2);
}

int	subtract(int val1, int val2)
{
	return (val1 - val2);
}

int	divide(int val1, int val2)
{
	return (val1 / val2);
}

int	multiply(int val1, int val2)
{
	return (val1 * val2);
}

int	modulo(int val1, int val2)
{
	return (val1 % val2);
}
