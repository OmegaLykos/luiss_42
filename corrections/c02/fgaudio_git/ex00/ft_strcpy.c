char	*ft_strcpy(char *dest, char *src)
{
	int	s_len;

	s_len = 0;
	while (src[s_len] != '\0')
	{
		dest[s_len] = src[s_len];
		s_len++;
	}
	dest[s_len] = '\0';
	return (dest);
}
