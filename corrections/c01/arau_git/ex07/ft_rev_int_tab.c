void	ft_rev_int_tab(int *tab, int size)
{
	int	c;
	int	i;

	c = 0;
	while (c < size)
	{
		i = *(tab + size - c);
		*(tab + size - c) = *(tab + c);
		*(tab + c) = i;
		c = c + 1;
	}
}
