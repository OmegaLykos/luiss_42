void	ft_rev_int_tab(int *tab, int size)
{
	int	i;
	int	j;
	int	box;

	i = 0;
	box = 0;
	j = size - 1;
	while (i < size / 2)
	{
		box = tab[i];
		tab[i] = tab[j];
		tab[j] = box;
		i++;
		j--;
	}
}
